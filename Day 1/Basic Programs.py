#Basic Program to print numbers

#Example 1:

mystring = "hello"
myfloat = 10.0
myint = 20

# testing code
if mystring == "hello":
    print("String: %s" % mystring)
if myfloat == float(10.0):
    print("float: %f" % myfloat)
if myint == 20:
    print("Integer: %d" % myint)
    
